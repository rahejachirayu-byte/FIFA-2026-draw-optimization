def _confederation_ok(groups, gi, conf):
    """Real FIFA 2026 rule (simplified): max 2 UEFA per group, max 1 from any
    other confederation per group."""
    confs_here = [c for _, c in groups[gi]]
    if conf == "UEFA":
        return confs_here.count("UEFA") < 2
    return conf not in confs_here


def _place_pot(groups, pot_teams, target_size):
    """Backtracking placement of a pot. Each group must reach target_size after
    this pot is placed."""
    teams = list(pot_teams)
    random.shuffle(teams)

    def recurse(ti):
        if ti == len(teams):
            return True
        team, conf = teams[ti]
        # Try each group that still needs a team from this pot
        candidates = [gi for gi in range(12) if len(groups[gi]) == target_size - 1]
        random.shuffle(candidates)
        for gi in candidates:
            if _confederation_ok(groups, gi, conf):
                groups[gi].append((team, conf))
                if recurse(ti + 1):
                    return True
                groups[gi].pop()
        return False

    # Groups that still need a team from this pot are those currently at size target_size-1
    return recurse(0)


def simulate_one_draw():
    """Return a list of 12 groups (each 4 (team,conf) tuples), or None."""
    groups = [[] for _ in range(12)]

    # Place hosts first
    host_placed = set()
    for host_team, letter in HOST_SLOTS.items():
        gi = GROUP_LETTERS.index(letter)
        conf = teams_df.loc[teams_df["team"] == host_team, "confederation"].iat[0]
        groups[gi].append((host_team, conf))
        host_placed.add(host_team)

    # Remaining Pot 1 teams fill the other 9 groups
    pot1_rest = [(t, c) for p, t, c in TEAMS if p == 1 and t not in host_placed]
    empty_groups = [gi for gi in range(12) if not groups[gi]]
    random.shuffle(pot1_rest)

    def place_pot1_rest(ti, empties):
        if ti == len(pot1_rest):
            return True
        team, conf = pot1_rest[ti]
        candidates = list(empties)
        random.shuffle(candidates)
        for gi in candidates:
            if _confederation_ok(groups, gi, conf):
                groups[gi].append((team, conf))
                new_empties = [g for g in empties if g != gi]
                if place_pot1_rest(ti + 1, new_empties):
                    return True
                groups[gi].pop()
        return False

    if not place_pot1_rest(0, empty_groups):
        return None

    # Pots 2, 3, 4
    for pot_n in (2, 3, 4):
        pot_teams = [(t, c) for p, t, c in TEAMS if p == pot_n]
        if not _place_pot(groups, pot_teams, pot_n):
            return None

    return groups


def run_simulation(n_target=2000, max_attempts=20000):
    """Run until we have n_target valid draws or hit max_attempts."""
    valid = []
    attempts = 0
    while len(valid) < n_target and attempts < max_attempts:
        attempts += 1
        g = simulate_one_draw()
        if g is not None:
            valid.append(g)
    return valid, attempts


