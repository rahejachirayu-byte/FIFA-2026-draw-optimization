def allocation_to_revenue(venue_alloc: pd.DataFrame) -> pd.DataFrame:
    """Given a venue allocation DataFrame with stage columns, compute proxy revenue."""
    rows = []
    for _, v in venue_alloc.iterrows():
        for _, s in stage_df.iterrows():
            n = int(v[s["alloc_col"]])
            if n <= 0:
                continue
            cap_scale = _cap_factor(v["capacity"])
            per_match = s["median_proxy_revenue_usd"] * cap_scale
            rows.append({
                "venue": v["venue"],
                "country": v["country"],
                "stage": s["stage"],
                "n_matches": n,
                "revenue": per_match * n,
            })
    return pd.DataFrame(rows)


def scenario_revenue_first(base: pd.DataFrame) -> pd.DataFrame:
    """Aggressively concentrate high-value matches in the highest-capacity
    venues. Move knockouts toward 80k+ venues and shift some group matches
    away from smaller hosts."""
    out = base.copy()

    def idx(name):
        return out.index[out["venue"] == name][0]

    # Move 1 QF from smaller knockout hosts to the largest venues
    for donor, recipient in [
        ("Arrowhead Stadium", "AT&T Stadium"),
        ("Mercedes-Benz Stadium", "MetLife Stadium"),
    ]:
        if out.at[idx(donor), "qf_matches"] > 0:
            out.at[idx(donor), "qf_matches"] -= 1
            out.at[idx(recipient), "qf_matches"] += 1

    # Move 1 RO16 from smallest-capacity venues to 80k-club
    for donor, recipient in [
        ("BMO Field", "MetLife Stadium"),
        ("Estadio Akron", "AT&T Stadium"),
        ("Estadio BBVA", "SoFi Stadium"),
    ]:
        if out.at[idx(donor), "ro16_matches"] > 0:
            out.at[idx(donor), "ro16_matches"] -= 1
            out.at[idx(recipient), "ro16_matches"] += 1

    # Group match reshuffle: take 2 from small venues, give to big ones
    for donor, recipient in [
        ("Estadio Akron", "AT&T Stadium"),
        ("BMO Field", "MetLife Stadium"),
    ]:
        if out.at[idx(donor), "group_matches"] > 0:
            out.at[idx(donor), "group_matches"] -= 1
            out.at[idx(recipient), "group_matches"] += 1

    return out


def scenario_fairness(base: pd.DataFrame) -> pd.DataFrame:
    """Protect and elevate smaller host markets. Shift matches from dominant
    US venues to Canadian and Mexican hosts, accepting lower per-match revenue
    in exchange for more even host-country distribution."""
    out = base.copy()

    def idx(name):
        return out.index[out["venue"] == name][0]

    # Move group matches from top 3 US venues toward Canadian/Mexican hosts
    donors = ["MetLife Stadium", "AT&T Stadium", "Mercedes-Benz Stadium"]
    recipients = ["BMO Field", "BC Place", "Estadio Akron",
                  "Estadio BBVA", "Estadio Azteca"]
    for d, r in zip(donors, recipients[:3]):
        if out.at[idx(d), "group_matches"] > 0:
            out.at[idx(d), "group_matches"] -= 1
            out.at[idx(r), "group_matches"] += 1

    # Move a RO16 match from a US venue to Estadio Azteca (historic fairness gesture)
    if out.at[idx("Lincoln Financial Field"), "ro16_matches"] > 0:
        out.at[idx("Lincoln Financial Field"), "ro16_matches"] -= 1
        out.at[idx("Estadio Azteca"), "ro16_matches"] += 1

    # One extra group match to BBVA Monterrey
    out.at[idx("Levi's Stadium"), "group_matches"] = max(
        0, out.at[idx("Levi's Stadium"), "group_matches"] - 1
    )
    out.at[idx("Estadio BBVA"), "group_matches"] += 1

    return out


def scenario_low_travel(base: pd.DataFrame) -> pd.DataFrame:
    """Concentrate group-stage matches in regional clusters to reduce
    intra-tournament travel. Move far-flung venue matches toward hubs in
    each host country."""
    out = base.copy()

    def idx(name):
        return out.index[out["venue"] == name][0]

    # Move group matches out of geographic edges into central hubs
    for donor, recipient in [
        ("Lumen Field", "Levi's Stadium"),       # Seattle -> SF
        ("Hard Rock Stadium", "Mercedes-Benz Stadium"),  # Miami -> Atlanta
        ("Gillette Stadium", "MetLife Stadium"),  # Boston -> NYC
    ]:
        if out.at[idx(donor), "group_matches"] > 0:
            out.at[idx(donor), "group_matches"] -= 1
            out.at[idx(recipient), "group_matches"] += 1

    return out


def scenario_balanced(base: pd.DataFrame) -> pd.DataFrame:
    """A measured compromise: small revenue lift, modest fairness gain."""
    out = base.copy()

    def idx(name):
        return out.index[out["venue"] == name][0]

    # Bump iconic + underused hosts symbolically
    out.at[idx("Estadio Azteca"), "group_matches"] += 1
    out.at[idx("BC Place"), "group_matches"] += 1

    # Take from two smaller-capacity US venues (keeps revenue roughly neutral)
    for d in ["Lincoln Financial Field", "Levi's Stadium"]:
        if out.at[idx(d), "group_matches"] > 0:
            out.at[idx(d), "group_matches"] -= 1

    return out


def compute_policy_metrics(alloc: pd.DataFrame, label: str) -> dict:
    rev = allocation_to_revenue(alloc)
    by_venue = rev.groupby("venue", as_index=False)["revenue"].sum()
    by_country = rev.groupby("country", as_index=False)["revenue"].sum()
    total = rev["revenue"].sum()

    # Host-country equity: 1 - coefficient of variation across countries
    country_rev = by_country["revenue"].values
    cv = country_rev.std() / max(country_rev.mean(), 1)
    equity_score = max(0.0, 1 - cv / 2)  # scaled to roughly [0, 1]

    # Utilization: weighted average of capacity vs stage avg attendance
    util_parts = []
    for _, v in alloc.iterrows():
        total_m = sum(int(v[c]) for c in [
            "group_matches", "ro32_matches", "ro16_matches",
            "qf_matches", "sf_matches", "third_matches", "final_matches"
        ])
        if total_m == 0:
            continue
        cap = v["capacity"]
        # blended stage avg attendance weighted by that venue's mix
        for _, s in stage_df.iterrows():
            n = int(v[s["alloc_col"]])
            if n == 0:
                continue
            fill = min(1.0, s["avg_attendance"] / cap)
            util_parts.append((fill, n))
    util = (
        sum(f * n for f, n in util_parts) / max(sum(n for _, n in util_parts), 1)
    )

    # Travel proxy: higher when group matches are more geographically dispersed.
    # Use the count of distinct regions touched by group matches (regions are
    # simplified as country + US region groups).
    US_REGIONS = {
        "MetLife Stadium": "US-NE", "Gillette Stadium": "US-NE",
        "Lincoln Financial Field": "US-NE",
        "Mercedes-Benz Stadium": "US-SE", "Hard Rock Stadium": "US-SE",
        "AT&T Stadium": "US-SC", "NRG Stadium": "US-SC",
        "Arrowhead Stadium": "US-MW",
        "SoFi Stadium": "US-W", "Levi's Stadium": "US-W", "Lumen Field": "US-W",
    }
    region_counts = {}
    for _, v in alloc.iterrows():
        gm = int(v["group_matches"])
        if gm == 0:
            continue
        region = US_REGIONS.get(v["venue"], v["country"])
        region_counts[region] = region_counts.get(region, 0) + gm
    travel_proxy = len(region_counts)  # number of distinct group-stage regions (lower = less travel)

    return {
        "scenario": label,
        "total_revenue_usd": round(total),
        "revenue_change_vs_baseline_pct": None,  # filled later
        "equity_score": round(equity_score, 3),
        "utilization_proxy": round(util, 3),
        "travel_regions_touched": travel_proxy,
        "top_country": by_country.sort_values("revenue", ascending=False).iloc[0]["country"],
        "top_country_share": round(
            by_country.sort_values("revenue", ascending=False).iloc[0]["revenue"] / total, 3
        ),
    }


